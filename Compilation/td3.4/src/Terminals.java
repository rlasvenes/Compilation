/**
 * This class lists terminals used by the
 * grammar specified in "ParserLogic.grammar".
 */
public class Terminals {
	static public final short EOF = 0;
	static public final short LPARENTH = 1;
	static public final short NEGATION = 2;
	static public final short TRUE = 3;
	static public final short FALSE = 4;
	static public final short VARIABLE = 5;
	static public final short AND = 6;
	static public final short RPARENTH = 7;
	static public final short OR = 8;
}
