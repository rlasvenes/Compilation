/**
 * This class lists terminals used by the
 * grammar specified in "ParserExpr.grammar".
 */
public class Terminals {
	static public final short EOF = 0;
	static public final short LPARENTH = 1;
	static public final short TOKEN_NUMBER = 2;
	static public final short MULT = 3;
	static public final short DIVIDE = 4;
	static public final short PLUS = 5;
	static public final short MINUS = 6;
	static public final short RPARENTH = 7;
}
